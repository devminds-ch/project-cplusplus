#include "calculate.hpp"

double Calculate::sum(const double &a, const double &b) { return a + b; }

double Calculate::subtract(const double &a, const double &b) { return a - b; }

double Calculate::multiply(const double &a, const double &b) { return a * b; }

double Calculate::divide(const double &a, const double &b) { return a / b; }
